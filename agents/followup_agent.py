STATUS_LABELS = {
    "new": "新线索",
    "interested": "有意向",
    "considering": "考虑中",
    "price_sensitive": "价格敏感",
    "no_show": "爽约未到",
    "silent": "静默失联",
    "contacted": "已联系",
    "busy": "时间冲突",
    "not_matching": "需求不匹配",
    "enrolled": "已报名",
}

STATUS_ORDER = ["new", "contacted", "interested", "considering", "price_sensitive", "busy", "no_show", "not_matching", "silent", "enrolled"]


def get_followup_strategy(status, name, course="", **kwargs):
    """获取跟进策略"""
    script_info = FOLLOWUP_SCRIPTS.get(status)
    if not script_info:
        script_info = FOLLOWUP_SCRIPTS["new"]

    script = script_info["script"].format(
        姓名=name or "同学",
        课程=course or "相关课程",
        **kwargs
    )

    return {
        "状态": STATUS_LABELS.get(status, "新线索"),
        "跟进标题": script_info["title"],
        "最佳时机": script_info["timing"],
        "推荐渠道": script_info["channel"],
        "话术模板": script.strip(),
        "注意事项": script_info["tips"],
    }


def get_status_label(status):
    return STATUS_LABELS.get(status, "未知")


def get_all_statuses():
    return [{"id": s, "label": STATUS_LABELS[s]} for s in STATUS_ORDER]
